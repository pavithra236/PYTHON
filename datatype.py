#data type
#string
x = "Hello World"
print(type(x))
#integer
x=5
print(type(x))
#float
x=0.005
print(type(x))
#complex
x=2j
print(type(x))
#list
x=["sugi","pavi","lavi"]
print(type(x))
#tuple
x = ("apple", "banana", "cherry")
print(type(x))
#range
x = range(6)
print(type(x))
#dict
x = {"name" : "John", "age" : 36}
print(type(x)) 
#frozenset
x = frozenset({"apple", "banana", "cherry"})
print(type(x)) 
#set
x = {"apple", "banana", "cherry"}
print(type(x))
#bool
x = True
print(type(x)) 
#bytearray
x = bytearray(5)
print(type(x)) 
#bytes
x = b"Hello"
print(type(x)) 
#memoryview
x = memoryview(bytes(5))
print(type(x)) 
#nonetype
x = None
print(type(x)) 
